import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc = new Scanner(System.in);
		int t=sc.nextInt();
		for(int i=0;i<t;i++){
		    int c=0;
		    int m=sc.nextInt();
		    String str = Integer.toString(m);
		    for(int j=0;j<str.length();j++){
		    if(str.charAt(j)=='4'){
		        c++;
		    }}
		    System.out.println(c);
		    
		}
	
	}
}
